export type TranslationKey = keyof typeof translations.en;

type Params = Record<string, string | number>;
type PluralCategory = 'one' | 'few' | 'many' | 'other';
type CountKind = 'stops' | 'datasets';
type DurationUnit = 'minute' | 'hour';
type RelativeDirection = 'future' | 'past';

const COUNT_TRANSLATION_KEYS = {
  stops: {
    one: 'countStopsOne',
    few: 'countStopsFew',
    many: 'countStopsMany',
    other: 'countStopsOther',
  },
  datasets: {
    one: 'countDatasetsOne',
    few: 'countDatasetsFew',
    many: 'countDatasetsMany',
    other: 'countDatasetsOther',
  },
} satisfies Record<CountKind, Record<PluralCategory, TranslationKey>>;

const DURATION_TRANSLATION_KEYS = {
  minute: {
    one: 'durationMinuteOne',
    few: 'durationMinuteFew',
    many: 'durationMinuteMany',
    other: 'durationMinuteOther',
  },
  hour: {
    one: 'durationHourOne',
    few: 'durationHourFew',
    many: 'durationHourMany',
    other: 'durationHourOther',
  },
} satisfies Record<DurationUnit, Record<PluralCategory, TranslationKey>>;

